%%%-------------------------------------------------------------------
%%% @doc
%%% 이 모듈은 Erlang의 기본 데이터 타입들을 상세하게 예시하는 코드입니다.
%%%
%%% 이 예제에서는 다음 데이터 타입들을 다룹니다:
%%% 1. 숫자 (정수, 부동소수점, 과학적 표기법)
%%% 2. 아톰 (상수 값, 식별자 역할)
%%% 3. 문자열 (문자 리스트)
%%% 4. 바이너리 (효율적인 바이너리 데이터 처리)
%%% 5. 튜플 (고정 크기 데이터 그룹)
%%% 6. 리스트 (가변 길이 데이터 시퀀스)
%%%
%%% 각 예제마다 상세한 주석을 통해 데이터 타입의 특징과 사용법을 설명합니다.
%%%
%%% @author
%%% @copyright 2025
%%%-------------------------------------------------------------------
-module(basic_types_examples).

%% 외부에서 호출할 수 있도록 기본 예제 함수를 export 합니다.
-export([basic_types_demo/0]).

%%%-------------------------------------------------------------------
%%% @doc
%%% basic_types_demo/0 함수는 위에서 설명한 각 데이터 타입의 예제를 생성하고,
%%% io:format/2 함수를 이용해 결과를 출력합니다.
%%%
%%% 각 데이터 타입에 대한 예제를 실행한 후, 최종 결과들을 하나의 튜플로 반환합니다.
%%%-------------------------------------------------------------------
basic_types_demo() ->
    %% ================================================================
    %% 1. 숫자 (Numbers)
    %% ================================================================
    %% 정수: Erlang에서 기본적인 정수값을 표현합니다.
    IntegerExample = 42,
    
    %% 부동소수점: 소수점을 포함하는 숫자로, 3.14159와 같이 표현합니다.
    FloatExample = 3.14159,
    
    %% 과학적 표기법: 매우 크거나 작은 수를 표현할 때 유용하며,
    %% 1.0e-3은 1.0 * 10^-3 즉, 0.001을 의미합니다.
    ScientificExample = 1.0e-3,
    
    %% ================================================================
    %% 2. 아톰 (Atoms)
    %% ================================================================
    %% 아톰은 값 자체가 그 이름입니다. 상수로서 사용되며,
    %% 첫 글자가 소문자인 경우 간단하게 사용 가능합니다.
    AtomExample1 = hello,
    
    %% 공백이나 특수문자가 포함된 경우에는 반드시 작은따옴표(')로 감싸야 합니다.
    AtomExample2 = 'Hello World',
    
    %% ================================================================
    %% 3. 문자열 (Strings)
    %% ================================================================
    %% Erlang에서 문자열은 실제로 문자들의 리스트입니다.
    %% "Hello, Erlang!"은 각 문자(ASCII 코드 값)의 리스트로 구성됩니다.
    StringExample = "Hello, Erlang!",
    
    %% 동일한 문자열을 리스트 형태로 직접 구성할 수도 있습니다.
    %% 예: $H 는 문자 'H'의 ASCII 코드값을 나타냅니다.
    ListOfChars = [$H, $e, $l, $l, $o, $, , $ , E, r, l, a, n, g, !],
    
    %% ================================================================
    %% 4. 바이너리 (Binaries)
    %% ================================================================
    %% 바이너리는 << >> 구문을 사용하여 효율적으로 데이터를 저장하고 처리할 때 사용됩니다.
    %% 여기서는 "Hello"라는 문자열을 바이너리로 표현합니다.
    BinaryExample = <<"Hello">>,
    
    %% 또한, 직접 숫자 값을 지정하여 바이너리를 생성할 수 있습니다.
    %% 65, 66, 67은 각각 ASCII 코드로 'A', 'B', 'C'를 의미합니다.
    BinaryNumbers = <<65, 66, 67>>,
    
    %% ================================================================
    %% 5. 튜플 (Tuples)
    %% ================================================================
    %% 튜플은 고정된 개수의 요소를 가진 데이터 구조로,
    %% 여러 값을 하나의 그룹으로 묶어 표현할 때 유용합니다.
    %% 예를 들어, 2차원 좌표를 표현하는 튜플:
    TupleExample = {point, 10, 20},
    
    %% ================================================================
    %% 6. 리스트 (Lists)
    %% ================================================================
    %% 리스트는 크기가 가변적인 데이터 시퀀스로, 동일한 자료형 또는 혼합 자료형을 포함할 수 있습니다.
    %% 예를 들어, 정수로만 이루어진 리스트:
    ListExample = [1, 2, 3, 4, 5],
    
    %% 다양한 데이터 타입이 섞인 리스트의 예:
    MixedListExample = [42, hello, "Erlang", {tuple, element}],
    
    %% ================================================================
    %% 결과 출력 (Printing the Results)
    %% ================================================================
    %% io:format/2 함수를 사용하여 각 데이터 타입의 예제를 콘솔에 출력합니다.
    io:format("~n--- Erlang 기본 데이터 타입 예제 ---~n", []),
    io:format("1. 정수 예제: ~p~n", [IntegerExample]),
    io:format("2. 부동소수점 예제: ~p~n", [FloatExample]),
    io:format("3. 과학적 표기법 예제: ~p~n", [ScientificExample]),
    io:format("4. 아톰 예제: ~p, ~p~n", [AtomExample1, AtomExample2]),
    io:format("5. 문자열 예제: ~p~n", [StringExample]),
    io:format("6. 문자 리스트 예제: ~p~n", [ListOfChars]),
    io:format("7. 바이너리 예제: ~p~n", [BinaryExample]),
    io:format("8. 숫자 리스트를 통한 바이너리 예제 (\"ABC\"): ~p~n", [BinaryNumbers]),
    io:format("9. 튜플 예제: ~p~n", [TupleExample]),
    io:format("10. 리스트 예제: ~p~n", [ListExample]),
    io:format("11. 혼합 리스트 예제: ~p~n", [MixedListExample]),
    
    %% 최종적으로 모든 예제 결과들을 하나의 튜플로 반환합니다.
    {basic_types_demo,
     IntegerExample, FloatExample, ScientificExample,
     AtomExample1, AtomExample2, StringExample, ListOfChars,
     BinaryExample, BinaryNumbers, TupleExample, ListExample,
     MixedListExample}.
